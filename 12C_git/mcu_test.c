void main(void)
{
	Init_MCU();
	Init_Parameter();
	UartInit();
	Delay_xMs(30000);
	while(1)
	{
		 Trig_SuperSonic();         //触发超声波发射
		 while(INPUT == 0)          //等待回声
     	{
        	;
     	}
		 Measure_Distance();        //计算脉宽并转换为距离
		 show();
		 UART1_Send_String("distance = ");
		 UART1_Send_String(a);
		 UART1_Send_String("\n");
		 Init_Parameter();          // 参数重新初始化
		 delayt(10);               //延时，两次发射之间要至少有10ms间隔
	 }	
}

